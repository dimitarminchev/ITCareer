/*
Задача 3.8. Намерете имената на всички служители със заплата в диапазон
Напишете SQL заявка, която намира личното име, фамилно име и длъжност на всички служители, чиято заплата е в диапазона [20000, 30000].  Изпратете вашите заявки като Подготвите БД & изпълните заявките (Prepare DB & run queries.)
